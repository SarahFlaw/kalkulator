<?php
// Class Kalkulator
class kalkulator{

  function tambah($a, $b){ // Fungsi Pertambahan
    $out = $a + $b;
    return $out;
  }
  
  function kurang($a, $b){ // Fungsi Pengurangan
    $out = $a - $b;
    return $out;
  }
  
  function bagi($a, $b){ // Fungsi Pembagian
    $out = $a / $b;
    return $out;
  }
  
  function kali($a, $b){ // Fungsi Perkalian
    $out = $a * $b;
    return $out;
  }
  
}
